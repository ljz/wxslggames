class Main extends egret.DisplayObjectContainer {

    private times:number;
    private webSocket:egret.WebSocket;

    public constructor() {
        super();
        this.addEventListener(egret.Event.ADDED_TO_STAGE, this.onAddToStage, this);
        this.times = -1;
    }

    private onAddToStage(event: egret.Event) {
        //这里没有加载资源的代码呢?
        //放到runGame里面的loadResource里面了.

        egret.lifecycle.addLifecycleListener((context) => {
            // custom lifecycle plugin

            context.onUpdate = () => {

            }
        })

        egret.lifecycle.onPause = () => {
            egret.ticker.pause();
        }

        egret.lifecycle.onResume = () => {
            egret.ticker.resume();
        }

        this.runGame().catch(e => {
            console.log(e);
        })



    }

    private async runGame() {
        await this.loadResource()
        this.createGameScene();
        const result = await RES.getResAsync("description_json");
        // console.log(">>>>>>", result);
        this.startAnimation(result);
        await platform.login();
        const userInfo = await platform.getUserInfo();
        console.log(userInfo);

    }

    private async loadResource() {
        try {
            const loadingView = new LoadingUI();
            this.stage.addChild(loadingView);
            await RES.loadConfig("resource/default.res.json", "resource/");
            await RES.loadGroup("preload", 0, loadingView);
            this.stage.removeChild(loadingView);
        }
        catch (e) {
            console.error(e);
        }
    }

    private textfield: egret.TextField;

    /**
     * 创建游戏场景
     * Create a game scene
     */
    private createGameScene() {
        // var bg1:egret.Shape = new egret.Shape();
        // bg1.graphics.beginFill(0x336699);
        // bg1.graphics.drawRect(0, 0, this.stage.stageWidth, this.stage.stageHeight);
        // bg1.graphics.endFill();
        // this.addChild(bg1);


        // console.log("Hello World1112222", this.stage.stageWidth, this.stage.stageHeight);
        
        // var tx:egret.TextField = new egret.TextField();
        // tx.text = "hello world";
        // tx.size = 32;
        // tx.x = 20;
        // tx.y = 20;
        // tx.width = this.stage.stageWidth;
        // tx.touchEnabled = true;
        // tx.addEventListener(egret.TouchEvent.TOUCH_TAP, this.touchHandler, this);

        // this.stage.scaleMode = egret.StageScaleMode.FIXED_WIDTH;
        // this.addChild(tx);
        this.webSocket = new egret.WebSocket();
        this.webSocket.addEventListener(egret.ProgressEvent.SOCKET_DATA, this.onReceiveMessage, this);
        this.webSocket.addEventListener(egret.Event.CONNECT, this.onSocketOpen, this);
        this.webSocket.connect("echo.websocket.org", 80);
        var self = this;
        this.stage.addEventListener(egret.TouchEvent.TOUCH_TAP, function(){
            switch(++self.times %3){
                case 0: 
                    egret.Tween.get(sky).to({x:-20, y:-29}, 300, egret.Ease.circIn);
                    break;
                case 1: break;
                case 2: break;
            }
        }, this);


        let sky = this.createBitmapByName("bg_jpg");
        this.addChild(sky);
        let stageW = this.stage.stageWidth;
        let stageH = this.stage.stageHeight;
        sky.width = stageW;
        sky.height = stageH;

        let topMask = new egret.Shape();
        topMask.graphics.beginFill(0x000000, 0.5);
        topMask.graphics.drawRect(0, 0, stageW, 172);
        topMask.graphics.endFill();
        topMask.y = 33;
        this.addChild(topMask);

        let icon = this.createBitmapByName("egret_icon_png");
        this.addChild(icon);
        icon.x = 26;
        icon.y = 33;

        let line = new egret.Shape();
        line.graphics.lineStyle(2, 0xffffff);
        line.graphics.moveTo(0, 0);
        line.graphics.lineTo(0, 117);
        line.graphics.endFill();
        line.x = 172;
        line.y = 61;
        this.addChild(line);


        let colorLabel = new egret.TextField();
        colorLabel.textColor = 0xffffff;
        colorLabel.width = stageW - 172;
        colorLabel.textAlign = "center";
        colorLabel.text = "Hello Egret";
        colorLabel.size = 24;
        colorLabel.x = 172;
        colorLabel.y = 80;
        this.addChild(colorLabel);

        let textfield = new egret.TextField();
        this.addChild(textfield);
        textfield.alpha = 0;
        textfield.width = stageW - 172;
        textfield.textAlign = egret.HorizontalAlign.CENTER;
        textfield.size = 24;
        textfield.textColor = 0xffffff;
        textfield.x = 172;
        textfield.y = 135;
        this.textfield = textfield;


    }

    private onReceiveMessage(e:egret.Event):void{
        var msg = this.webSocket.readUTF();
        console.log("Receive data:"+ msg);
    }

    private onSocketOpen():void{
        var cmd = "Hello Egret Socket";
        console.log("The connection is successful, send data:"+cmd);
        this.webSocket.writeUTF(cmd);
    }
    private touchHandler(evt:egret.TouchEvent):void{
        var tx:egret.TextField = evt.currentTarget;
        tx.textColor = 0x00ff00;
    }
    /**
     * 根据name关键字创建一个Bitmap对象。name属性请参考resources/resource.json配置文件的内容。
     * Create a Bitmap object according to name keyword.As for the property of name please refer to the configuration file of resources/resource.json.
     */
    private createBitmapByName(name: string) {
        let result = new egret.Bitmap();
        let texture: egret.Texture = RES.getRes(name);
        result.texture = texture;
        return result;
    }

    /**
     * 描述文件加载成功，开始播放动画
     * Description file loading is successful, start to play the animation
     */
    private startAnimation(result: string[]) {
        let parser = new egret.HtmlTextParser();

        let textflowArr = result.map(text => parser.parse(text));
        let textfield = this.textfield;
        let count = -1;
        let change = () => {
            count++;
            if (count >= textflowArr.length) {
                count = 0;
            }
            let textFlow = textflowArr[count];

            // 切换描述内容
            // Switch to described content
            textfield.textFlow = textFlow;
            let tw = egret.Tween.get(textfield);
            tw.to({ "alpha": 1 }, 200);
            tw.wait(2000);
            tw.to({ "alpha": 0 }, 200);
            tw.call(change, this);
        };

        change();
    }
}